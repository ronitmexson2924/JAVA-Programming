public class varaible_data_types{
    public static void main(String[] args) {
        //byte, short, int, long
        //byte - 8bit
        byte age = 36;
        //short - 16bit
        short year = 2024;  // Fixed: value within short range
        //int - 32bit
        int population = 33000000;
        //long - 64bit
        long worldPopulation = 80000000000L;  // Fixed: added L suffix
        //float
        float num = 25.26F;
        //double
        double pi = 3.14159265;
        //character printing
        char myChar = 'A';
        //Boolean is a data type which allows ud to store data either in true or false 
        boolean isjavaCool = true;
        //String
        String greeting = "Hola, Como es tas";
        System.out.println(myChar);
        System.out.println(age);
        System.out.println(year);
        System.out.println(population);
        System.out.println(worldPopulation);
        System.out.println(num);
        System.out.println(pi);
        System.out.println(isjavaCool);
        System.out.println(greeting); 
    }
}
